class Advertisement:
    def __init__(self, id, title, price, price_per_sqm, property_level, total_bedrooms, total_bathrooms, construction_year, area, link):
        self.id = id
        self.title = title
        self.price = price
        self.price_per_sqm = price_per_sqm
        self.property_level = property_level
        self.total_bedrooms = total_bedrooms
        self.total_bathrooms = total_bathrooms
        self.construction_year = construction_year
        self.area = area
        self.link = link
